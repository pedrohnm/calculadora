    private void btnIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIgualActionPerformed
        
        lblNumeroAnterior.setText("");
        lblOperador.setText("");
        
        if (contador.equals(0)) {
            num2 = Double.valueOf(lblResultado.getText());
        }
        
        switch (operador) {
            
            case "+":
                if (contador.equals(0)) {
                    lblResultado.setText(String.valueOf(num1 + num2));
                } else {
                    lblResultado.setText(String.valueOf(resultado + num2));
                }
                break;
                
            case "-":
                if (contador.equals(0)) {
                    lblResultado.setText(String.valueOf(num1 - num2));
                } else {
                    lblResultado.setText(String.valueOf(resultado - num2));
                }
                break;
                
            case "*":
                if (contador.equals(0)) {
                    lblResultado.setText(String.valueOf(num1 * num2));
                } else {
                    lblResultado.setText(String.valueOf(resultado * num2));
                }
                break;
                
            case "/":
                if (contador.equals(0)) {
                    lblResultado.setText(String.valueOf(num1 / num2));
                } else {
                    lblResultado.setText(String.valueOf(resultado / num2));
                }
                break;
        }
        
        resultado = Double.valueOf(lblResultado.getText());
        contador = 1;
    }//GEN-LAST:event_btnIgualActionPerformed

